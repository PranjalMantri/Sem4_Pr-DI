public class Main {
    public static void main(String[] args){
        System.out.println("This is String ");

        int a = 5;
        System.out.println("This is Integer: " + a);

        float b = 5.6f;
        System.out.println("This is Float: " + b);

        double c = 5.678d;
        System.out.println("This is Double: " + c);

        short d = 768;
        System.out.println("This is Short: " + d);

        long e = 808;
        System.out.println("This is Long: " + e);

        byte f = -128;
        System.out.println("This is Byte: " + f);

        boolean g = true;
        System.out.println("This is Boolean: " + g);

        char h = 'a';
        System.out.println("This is Char: " + h);
    }
}